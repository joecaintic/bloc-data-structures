class MyStack
    
    example_array = []
    
    def push(element)
        example_array.length + 1 = example_array.length
        example_array[example_array.length - 1] = element
        example_array
    end
    
    def pop
        example_array[example_array.length - 1] = nil
        example_array.length - 1 = example_array.length
        example_array
    end

end